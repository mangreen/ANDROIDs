package com.iiiesti.walkmap.customType;

public class POI_SimplePoint {
	
	/* Members */
	public int latE6;
	public int lonE6;
	public int classIdx;
	public String name;
	public String info;
	public String buildID;
	
	public POI_SimplePoint(String poiName, String poiInfo, int latitudeE6, int longitudeE6, String buildIdx, int poiClass) {
		name = poiName;
		info = poiInfo;
		latE6 = latitudeE6;
		lonE6 = longitudeE6;
		classIdx = poiClass;
		buildID = buildIdx;
	}
}
